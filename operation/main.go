package main

import "fmt"

func main() {
	num1 := 10
	num2 := 5

	// start operations
	sum := num1 + num2
	dif := num1 - num2
	product := num1 * num2
	quotient := num1 / num2

	// display output
	fmt.Printf("The sum is %d\n", sum)
	fmt.Printf("The dif is %d\n", dif)
	fmt.Printf("The product is %d\n", product)
	fmt.Printf("The quotient is %d\n", quotient)
}
